class Solution:
    def getLastDigit(self, a, b):
        vals = {
        0: {1:0, 2:0, 3:0, 0:0},
        1: {1:1, 2:1, 3:1, 0:1},
        2: {1:2, 2:4, 3:8, 0:6},
        3: {1:3, 2:9, 3:7, 0:1},
        4: {1:4, 2:6, 3:4, 0:6},
        5: {1:5, 2:5, 3:5, 0:5},
        6: {1:6, 2:6, 3:6, 0:6},
        7: {1:7, 2:9, 3:3, 0:1},
        8: {1:8, 2:4, 3:2, 0:6},
        9: {1:9, 2:1, 3:9, 0:1}}
        
        a = int(a)
        b = int(b)
        
        if b == 0:
            return 1
            
        a = a % 10
        rem = b % 4
        
        return vals[a][rem]
        